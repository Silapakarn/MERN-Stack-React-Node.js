public class AdvancedCalculator extends Calculator {
    public String sin(double v){
        return "Sin Result: " + Math.sin(v);
    }

    public String cos(double v){
        return "Sin Result: " + Math.cos(v);
    }

    public String tan(double v){
        return "Sin Result: " + Math.tan(v);
    }
}
